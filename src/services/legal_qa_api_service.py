_HTTP_URL_ADAPTER = TypeAdapter(HttpUrl)

LegalQATimingLogger = Callable[[str, str | None, int, int, str | None], None]


class LegalQAWorkflowDecision(StrEnum):
    """Internal decision values returned by a legal QA workflow."""

    ANSWERED = "answered"
    ANSWERED_WITH_CAUTION = "answered_with_caution"
    FALLBACK = "fallback"


@dataclass(frozen=True)
class LegalQAWorkflowRequest:
    """Internal request passed from the API service to a legal QA workflow.

    Attributes:
        request_id: Opaque request identifier generated by the service boundary.
        question: Validated and stripped Vietnamese legal question.
        top_k: Requested evidence count for the workflow.
        include_debug: Reserved for future debug/admin behavior.
    """

    request_id: str
    question: str
    top_k: int
    context: PreparedLegalQAContext | None = None
    include_debug: bool = False
    timing_logger: LegalQATimingLogger | None = field(default=None, repr=False, compare=False)
    timing_started_at: float | None = field(default=None, repr=False, compare=False)


@dataclass(frozen=True)
class LegalQAWorkflowCitation:
    """Citation-ready child evidence reference returned by a workflow."""

    evidence_id: str
    chunk_id: str
    law_id: str
    law_name: str
    citation: str
    source_url: str
    hierarchy_path: str


@dataclass(frozen=True)
class LegalQAWorkflowEvidence:
    """Selected citable child evidence returned by a workflow."""

    evidence_id: str
    chunk_id: str
    law_id: str
    law_name: str
    citation: str
    text: str
    source_url: str
    score: float


@dataclass(frozen=True)
class LegalQAWorkflowMetadata:
    """Workflow metadata safe to expose through the public API response."""

    retrieval_strategy: str = "coverage_aware_quota"
    model: str | None = None
    reranking_used: bool = False
    latency_ms: int = 0
    conversation_context_used: bool = False
    conversation_context_message_count: int = 0
    follow_up_detected: bool = False
    retrieval_question_prepared: bool = False
    retrieval_mode: str = "hybrid"
    dense_retrieval_used: bool = False
    dense_retrieval_fallback_used: bool = False
    fallback_used: bool = False
    retriever_stage_failed: str | None = None
    embedding_model_cache_hit: bool = False
    embedding_model_loaded_before_request: bool = False
    model_cache_key: str | None = None


@dataclass(frozen=True)
class LegalQAWorkflowResult:
    """Internal result returned by a legal QA workflow.

    The result may represent an answered or fallback decision. Citations and evidence
    must describe selected child evidence only; parent context is auxiliary and not
    directly citable.
    """

    decision: LegalQAWorkflowDecision
    answer: str
    citations: list[LegalQAWorkflowCitation] = field(default_factory=list)
    evidence: list[LegalQAWorkflowEvidence] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)
    metadata: LegalQAWorkflowMetadata = field(default_factory=LegalQAWorkflowMetadata)


class LegalQAWorkflow(Protocol):
    """Protocol for future real or fake legal QA workflows."""

    def run(self, request: LegalQAWorkflowRequest) -> LegalQAWorkflowResult:
        """Run legal QA for a validated internal request.

        Args:
            request: Internal legal QA workflow request.

        Returns:
            Internal workflow result ready for API response mapping.
        """
        ...


@dataclass(frozen=True)
class LegalQAWarmupResult:
    """Sanitized warmup result for explicit embedding initialization."""

    warmed: bool
    elapsed_ms: int
    exception_class: str | None = None
    model_path_configured: bool = False
    model_path_exists: bool = False
    required_files_present: bool = False
    model_load_started: bool = False
    model_load_completed: bool = False
    model_load_timeout: bool = False
    encode_started: bool = False
    encode_completed: bool = False
    encode_timeout: bool = False
    cache_hit_before: bool = False
    cache_hit_after: bool = False
    model_cache_key: str | None = None


class LegalQAServiceError(RuntimeError):
    """Sanitized internal service failure with safe diagnostic metadata."""

    def __init__(
        self,
        *,
        failure_stage: str,
        original_exception: BaseException,
        request_id: str | None = None,
    ) -> None:
        self.failure_stage = _safe_failure_stage(original_exception, default=failure_stage)
        self.request_id = request_id
        self.exception_class = (
            type(original_exception).__name__
            if isinstance(original_exception, TimeoutError)
            else _root_exception_class(original_exception)
        )
        super().__init__(
            "Legal QA service failed "
            f"at stage {self.failure_stage} with exception class {self.exception_class}"
        )


